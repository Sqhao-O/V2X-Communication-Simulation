function [Pd_opt, Pc_opt] = calOptPower_nonrobust(sig2, Pc_max, Pd_max, ...
    alpha_k, alpha_mk, h_k, h_mk, gamma0)
% CALOPTPOWER_NONROBUST  非鲁棒功率分配算法（假设完美 CSI）
%
% 说明：
%   本算法作为对比基准，假设信道估计值即为真实信道（不考虑反馈延迟）。
%   功率分配策略为：V2I 链路优先使用最大功率 Pc_max 以最大化容量，
%   V2V 链路仅分配满足其 SINR 阈值所需的最小功率。
%
%   与鲁棒算法（calOptPower.m）相比，非鲁棒算法在存在 CSI 误差时
%   会显著低估 V2V 的实际干扰，导致 V2V 中断概率远高于设计阈值。
%
% 输入参数：
%   sig2     - 标量，噪声功率（线性值，单位 W）
%              例如：-114 dBm 对应 10^(-114/10) * 1e-3 ≈ 4e-12 W
%   Pc_max   - 标量，V2I/CUE 用户最大发射功率（线性值，单位 W）
%              例如：23 dBm 对应 10^(23/10) * 1e-3 ≈ 0.20 W
%   Pd_max   - 标量，V2V/DUE 用户最大发射功率（线性值，单位 W）
%   alpha_k  - 标量，V2V 直连链路的大尺度衰落因子（线性值）
%              由 genPL('V2V', ...) 输出的 combinedPL 转换而来：
%              alpha_k = 10^(PL_dB/10)
%   alpha_mk - 标量，V2I→V2V 干扰链路的大尺度衰落因子（线性值）
%   h_k      - 标量（复数），V2V 直连链路的估计小尺度信道增益
%              服从 CN(0,1)，即 (randn+1j*randn)/sqrt(2)
%   h_mk     - 标量（复数），V2I→V2V 干扰链路的估计小尺度信道增益
%              服从 CN(0,1)
%   gamma0   - 标量，V2V 链路的 SINR 最低阈值（线性值）
%              例如：5 dB 对应 10^(5/10) ≈ 3.16
%
% 输出参数：
%   Pc_opt   - 标量，V2I/CUE 最优发射功率（线性值，≤ Pc_max）
%   Pd_opt   - 标量，V2V/DUE 最优发射功率（线性值，≤ Pd_max）
%
% 算法逻辑：
%   Step 1：计算信道增益
%             g_k  = alpha_k  * |h_k|^2   （V2V 直连链路）
%             g_mk = alpha_mk * |h_mk|^2  （V2I→V2V 干扰链路）
%   Step 2：若 g_k ≤ 0（信道完全无效），令 Pd_opt=0，Pc_opt=Pc_max。
%   Step 3：V2I 使用最大功率：Pc_opt = Pc_max。
%   Step 4：计算满足 V2V 约束所需的最小 V2V 功率：
%             约束：Pd * g_k / (sig2 + Pc_opt * g_mk) ≥ gamma0
%             推导：Pd_needed = gamma0 * (sig2 + Pc_opt * g_mk) / g_k
%   Step 5：若 Pd_needed ≤ Pd_max，则 V2V 取最小所需功率（刚好满足约束），
%            V2I 取 Pc_max；否则 V2V 取最大功率，V2I 功率下调至刚好
%            满足 V2V 约束的最低值。
%
% 与鲁棒算法的关键差异：
%   - 鲁棒算法：考虑信道估计误差，在最坏情况下保证中断概率 ≤ p0
%   - 非鲁棒算法：忽略信道估计误差，直接基于估计信道设计功率，
%                当误差存在时 V2V 中断概率会大幅上升

    % -----------------------------------------------------------------
    % Step 1：计算信道增益（线性尺度）
    % -----------------------------------------------------------------
    % g_k：V2V 直连链路的总信道增益 = 大尺度衰落 × 小尺度衰落
    % |h_k|^2 服从指数分布（均值为 1），因为 h_k ~ CN(0,1)
    g_k  = alpha_k  * abs(h_k)^2;

    % g_mk：V2I→V2V 干扰链路的总信道增益
    g_mk = alpha_mk * abs(h_mk)^2;

    % -----------------------------------------------------------------
    % Step 2：边界情况 — 信道完全无效（g_k = 0 或接近零）
    % -----------------------------------------------------------------
    % 当 |h_k| = 0 时，V2V 链路完全无法通信，
    % 任何正功率都无法建立连接，因此令 Pd_opt = 0（全零功率）
    if g_k <= 0
        Pd_opt = 0;
        Pc_opt = Pc_max;  % V2I 仍使用最大功率
        return;
    end

    % -----------------------------------------------------------------
    % Step 3：V2I 链路优先 — 使用最大功率以提升系统总容量
    % -----------------------------------------------------------------
    Pc_opt = Pc_max;

    % -----------------------------------------------------------------
    % Step 4：V2V 链路仅分配最小所需功率（刚好满足 SINR 阈值）
    % -----------------------------------------------------------------
    % V2V 约束（基于估计信道）：
    %   Pd * g_k / (sig2 + Pc_opt * g_mk) >= gamma0
    % 求解 Pd 的下界：
    %   Pd_needed = gamma0 * (sig2 + Pc_opt * g_mk) / g_k
    %
    % 物理含义：只要发射 Pd_needed，V2V 的 SINR 就恰好等于 gamma0
    Pd_needed = gamma0 * (sig2 + Pc_opt * g_mk) / g_k;

    % -----------------------------------------------------------------
    % Step 5：功率分配决策
    % -----------------------------------------------------------------
    if Pd_needed <= Pd_max
        % 可行情况：最小所需功率不超过最大允许功率
        % 策略：V2V 使用刚好满足约束的最小功率（节能），
        %       V2I 使用最大功率（提升 V2I 容量）
        Pd_opt = Pd_needed;
    else
        % 不可行情况：即使以 Pd_max 发射，V2V 仍无法满足 SINR 阈值
        % 策略：V2V 使用最大功率，同时降低 V2I 功率以减少干扰，
        %       使 V2V 链路约束刚好满足
        Pd_opt = Pd_max;

        % 重新求解 Pc，使 V2V 约束在 Pd = Pd_max 时刚好满足：
        %   Pd_max * g_k / (sig2 + Pc * g_mk) = gamma0
        %   Pc_tmp = (Pd_max * g_k / gamma0 - sig2) / g_mk
        Pc_tmp = (Pd_max * g_k / gamma0 - sig2) / g_mk;

        % V2I 功率取重新计算的最小所需值（但不低于 0，不超过 Pc_max）
        Pc_opt = max(0, min(Pc_tmp, Pc_max));
    end

    % 确保输出非负（数值保护）
    Pd_opt = max(0, Pd_opt);
    Pc_opt = max(0, Pc_opt);

end
